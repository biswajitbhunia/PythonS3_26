backpack = ["laptop", "notebook", "pen"]

print("pen" in backpack)      # True
print("snacks" in backpack)   # False
print("snacks" not in backpack) # True
s="hello world"
print("hello" in s)            # True
print("world" in s)            # True
print("hi" in s)               # False
print("hi" not in s)           # True
print("Hello" in s)            # False (case-sensitive)