x = 10
y = 3

print(x + y)   # 13
print(x - y)   # 7
print(x * y)   # 30
print(x / y)   # 3.3333333333333335 (Normal division: Quotient)
print(x // y)  # 3 (Floor division: how many full times does 3 go into 10?)
print(x % y)   # 1 (Modulo: 10 divided by 3 is 9, with 1 left over)
print(x ** y)  # 1000 (10 to the power of 3)
